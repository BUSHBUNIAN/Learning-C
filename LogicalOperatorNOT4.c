#include<stdio.h>
#include<math.h>

//   You can either include the brackets or exclude them while placing multiple NOT Logical Operator (i.e !)

int main()
{
    printf("%d",!(6>7));  /*    Solving Procedure:  
    
    1. '6' is greater than '7'
    
    Output: FALSE(i.e 0)
    
    2. !FALSE; meanning NOT FALSE (i.e !0)
    
    Output: TRUE (i.e 1) This is the final output.   */
    
    return 0;
}
