#include<stdio.h>

// float data type operates with float data type will yield float data type in the output.

int main()
{
    float a,b;
    printf("\nEnter a=");
    scanf("%f",&a);
    printf("Enter b=");
    scanf("%f",&b);
    printf("a*b=%f\n",a*b);
    printf("a/b=%f\n",a/b);
    return 0;
}